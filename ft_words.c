#include "libft.h"

int		ft_words(char const *s, char c)
{
	int		i;
	int		num;

	num = 0;
	i = -1;
	while (s[++i] != '\0')
	{
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == '\0'))
			num++;
	}
	return (num);
}
